# Get an empty histogram with the binning for the standard dijet mass plot
def getMassHist( name, opt = "13TeV" ):
  bins = getMassBins( opt )
  hist = TH1F( name, name, len(bins)-1, array('f', bins) )
  hist.Sumw2()
  return hist

